package com.example.carpet.Model;

public class Carpet {

        private String title,image,caption,color,plan,size;
        private int id,code_carpet,shoulder,price;
        public String getTitle() {
            return title;
        }
        public void setTitle(String title) {
            this.title = title;
        }

    public String getImage() {
        return image;
    }
    public void setImage(String image) {
        this.image = image;
    }

    public String getCaption() {
        return caption;
    }
    public void setCaption(String caption) {
        this.caption = caption;
    }

    public String getColor() {
        return color;
    }
    public void setColor(String color) {
        this.color = color;
    }

    public String getPlan() {
        return plan;
    }
    public void setPlan(String plan) {
        this.plan = plan;
    }

    public String getSize() {
        return size;
    }
    public void setSize(String size) {
        this.size = size;
    }

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public int getCode_carpet() {
        return code_carpet;
    }
    public void setCode_carpet(int code_carpet) {
        this.code_carpet = code_carpet;
    }

    public int getShoulder() {
        return shoulder;
    }
    public void setShoulder(int shoulder) {
        this.shoulder = shoulder;
    }

    public int getPrice() {
        return price;
    }
    public void setPrice(int price) {
        this.price = price;
    }
}
